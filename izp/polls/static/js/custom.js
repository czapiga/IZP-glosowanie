document.getElementById("open-time-field").addEventListener("click", function (ev) {
    document.getElementById("open-time-field").classList.toggle("hidden-lg");
    document.getElementById("time-field").classList.toggle("hidden-lg");
    }, false);

document.getElementById("close-time-field").addEventListener("click", function (ev) {
    document.getElementById("open-time-field").classList.toggle("hidden-lg");
    document.getElementById("time-field").classList.toggle("hidden-lg");
    }, false);
